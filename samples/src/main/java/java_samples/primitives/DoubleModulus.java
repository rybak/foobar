package java_samples.primitives;

public class DoubleModulus {
    public static void main(String[] args) {
        System.out.println(12.2 % 1.0);

        System.out.println(-100 % 23);
        System.out.println(-100 % -23);
        System.out.println(-100 / -23);
        System.out.println(-100 / 23);
    }
}
